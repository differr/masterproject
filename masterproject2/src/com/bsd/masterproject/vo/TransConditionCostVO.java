package com.bsd.masterproject.vo;

public class TransConditionCostVO {
	private String nameSize;
	private double weightNotMoreThan;
	private double lengthBoxNotMoreThan;
	private double cost;
	private double distance;
	private double costPerKm;
	public String getNameSize() {
		return nameSize;
	}
	public void setNameSize(String nameSize) {
		this.nameSize = nameSize;
	}
	public double getWeightNotMoreThan() {
		return weightNotMoreThan;
	}
	public void setWeightNotMoreThan(double weightNotMoreThan) {
		this.weightNotMoreThan = weightNotMoreThan;
	}
	public double getLengthBoxNotMoreThan() {
		return lengthBoxNotMoreThan;
	}
	public void setLengthBoxNotMoreThan(double lengthBoxNotMoreThan) {
		this.lengthBoxNotMoreThan = lengthBoxNotMoreThan;
	}
	public double getCost() {
		return cost;
	}
	public void setCost(double cost) {
		this.cost = cost;
	}
	public double getDistance() {
		return distance;
	}
	public void setDistance(double distance) {
		this.distance = distance;
	}
	public double getCostPerKm() {
		return costPerKm;
	}
	public void setCostPerKm(double costPerKm) {
		this.costPerKm = costPerKm;
	}
	
	
	
}
